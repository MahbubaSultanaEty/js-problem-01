//  math methods : min, max, round, random, ceil, floor , pow, abs, sqrt,

// find the highest number
const max = Math.max(23, 45, 67, 89, 90);
console.log(max);

// find the lowest number
const min = Math.min(34, 67, 23, -34, -12, 34);
console.log(min);

// rounds the number to its nearest integar;
console.log(Math.round(23.56));

// gives a random number between 1 to 0;
console.log(Math.random());

// rounds up the number to its nearest integar
console.log(Math.ceil(4.37));

// rounds down the number to its nearest integar
console.log(Math.floor(34.76));

// calculate a base number raised to the power of an exponent
console.log(Math.pow(3, 4));

// find the squareRoot 
console.log(Math.sqrt(144));

// give the absolute value of the number [it convert any negative number to positive]
console.log(Math.abs(6-15));